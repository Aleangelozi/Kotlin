fun main() {

    // Mutable variables can be reassigned.
    var age = 30

    println(age) // It prints the current value of the variable.

    age = 45 // It reassigns the variable's value

    println(age) // It prints the current value of the variable.

    // Read-only variables can't be reassigned.
    val name = "Alessandro"

    println(name) // It prints the current value of the variable.

}